package org.demo.model;

public class Book {
	
	private String id;
	private String name;
	private String isbn;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	
	public String getIsbn() {
		return isbn;
	}
	public void setIsbn(String isbn) {
		this.isbn = isbn;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
